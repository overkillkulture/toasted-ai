    def _test_improvements(self, improvements: List[ForgeImprovement]) -> List[TestResult]:
        """Test improvements with quantum-inspired validation"""
        results = []
        
        for imp in improvements:
            # Simulate comprehensive testing
            result = TestResult(
                improvement_id=imp.improvement_id,
                integrity_score=random.uniform(0.85, 1.0),
                density_score=random.uniform(0.7, 1.0),
                speed_score=random.uniform(0.75, 1.0),
                quantum_score=random.uniform(0.8, 1.0),
                overall_score=0.0,
                passed=False
            )
            
            # Calculate weighted overall
            result.overall_score = (
                result.integrity_score * 0.35 +
                result.density_score * 0.25 +
                result.speed_score * 0.20 +
                result.quantum_score * 0.20
            ) * imp.maat_score
            
            # Must pass integrity and have decent overall
            result.passed = result.integrity_score > 0.9 and result.overall_score > 0.7
            
            results.append(result)
            self.test_results.append(result)
            
        return results
    
    def _approve_improvements(self, results: List[TestResult]) -> List[ForgeImprovement]:
        """Approve improvements that pass testing"""
        approved = []
        
        for result in results:
            if result.passed:
                # Find the improvement
                for imp in self.improvements:
                    if imp.improvement_id == result.improvement_id:
                        imp.tested = True
                        imp.approved = True
                        approved.append(imp)
                        break
        
        # Update best score
        if results:
            best = max(r.overall_score for r in results)
            if best > self.best_score:
                self.best_score = best
                
        return approved
    
    def get_forge_status(self) -> Dict[str, Any]:
        """Get forge status"""
        return {
            'generation': self.generation,
            'total_improvements': len(self.improvements),
            'approved_improvements': sum(1 for i in self.improvements if i.approved),
            'best_score': self.best_score,
            'latest_results': [
                {
                    'id': r.improvement_id,
                    'overall': r.overall_score,
                    'passed': r.passed
                }
                for r in self.test_results[-10:]
            ]
        }


